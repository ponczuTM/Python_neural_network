def cross(self):
    for x in range(100, 500):
        self.inputs.append(np.array([x, 300]))
        self.draw_point([x, 300], "darkgray", "darkgray")
    for y in range(100, 500):
        self.inputs.append(np.array([300, y]))
        self.draw_point([300, y], "darkgray", "darkgray")
    
def star(self):
    for j in range(200):
        radius = j
        center_x = 300
        center_y = 300
        angle_degrees = 360 / 5

        for i in range(5):
            angle = np.radians(i * angle_degrees)
            x = center_x + radius * np.cos(angle)
            y = center_y + radius * np.sin(angle)
            self.inputs.append(np.array([x, y]))
            self.draw_point([x, y], "darkgray", "darkgray")
